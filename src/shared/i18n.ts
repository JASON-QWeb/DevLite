export type I18nKey = keyof typeof UI_TEXT.zh;

export function uiText(locale: UiLocale | undefined, key: I18nKey): string {
  return UI_TEXT[normalizeUiLocale(locale)][key];
}

export function createTranslator(locale: UiLocale | undefined): (key: I18nKey) => string {
  return (key) => uiText(locale, key);
}

export function normalizeUiLocale(value: unknown): UiLocale {
  return value === "en" ? "en" : "zh";
}
